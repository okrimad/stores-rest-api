from db import db


class ItemModel(db.Model): # Extends db model

    # Telling SQLAlchemy about our tables and columns
    __tablename__ = 'items'

    id = db.Column(db.Integer, primary_key=True)
    name = db.Column(db.String(80))
    price = db.Column(db.Float(precision=2))
    store_id = db.Column(db.Integer, db.ForeignKey('stores.id'))
    store = db.relationship('StoreModel')

    def __init__(self, name, price, store_id):
        self.name = name
        self.price = price
        self.store_id = store_id

    def json(self):
        return {'name': self.name, 'price': self.price}

    # cls = reference to the class
    @classmethod
    def find_by_name(cls, name):
        # SELECT * FROM __tablename__ WHERE name = name(method_arg) LIMIT 1
        # using SQLAlchemy query builder and filter and return ItemModel object
        return cls.query.filter_by(name=name).first()

    def save_to_db(self):
        db.session.add(self)
        db.session.commit()

    def delete_from_db(self):
        db.session.delete(self)
        db.session.commit()